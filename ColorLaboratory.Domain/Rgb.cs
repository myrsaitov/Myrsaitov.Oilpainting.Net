namespace ColorLaboratory.Domain
{
    /// <summary>
    /// RGB color system
    /// </summary>
    public class Rgb
    {
        /// <summary>
        /// Red, 0...255
        /// </summary>
        public byte R { get; set; }
        /// <summary>
        /// Green, 0...255
        /// </summary>
        public byte G { get; set; }
        /// <summary>
        /// Blue, 0...255
        /// </summary>
        public byte B { get; set; }
    }
}
